#include <iostream>
using namespace std;

int main() {

    int a = 10;

    a += 5;             
    cout<< a <<endl;     //a = a + 5 => 15
    a -= 5;             
    cout<< a <<endl;     // a = a - 5 => 10
    a *= 5;
    cout<< a <<endl;      // a = a * 5 => 50
    a /= 5;
    cout<< a <<endl;      // a = a / 5 => 10


    return 0;
}
